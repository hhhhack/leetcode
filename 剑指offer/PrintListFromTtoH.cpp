#include <vector>
#include <algorithm>
#include "ComStruct.h"
using namespace std;

vector<int> printListFromTailToHead(ListNode* head) {
    vector<int> ret;
    while (head){
        ret.push_back(head->val);
        head = head->next;
    }
    reverse(ret.begin(), ret.end());
    return ret;
}
