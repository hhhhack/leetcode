#include "ComStruct.h"

ListNode* FindKthToTail(ListNode* pListHead, unsigned int k) {
    ListNode *ret = pListHead;
    while (pListHead){
        pListHead = pListHead->next;
        if (k == 0){
            ret = ret->next;
        }else{
            k--;
        }
    }
    if (k == 0){
        return ret;
    }
    return NULL;
}