#include "ComStruct.h"
using namespace std;
bool duplicate(int numbers[], int length, int* duplication) {
    vector<bool>ret(length, false);

    for (int i = 0; i < length; i ++){
        if (ret[numbers[i]]){
            *duplication = numbers[i];
            return true;
        }
        ret[numbers[i]] = true;
    }
    return false;
}