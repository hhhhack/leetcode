#include "ComStruct.h"

ListNode* ReverseList(ListNode* pHead) {
    ListNode *tail = NULL;
    ListNode *Head = NULL;
    ListNode *tmp;
    while(pHead){
        tmp = Head;
        Head = pHead;
        pHead = pHead->next;
        Head->next = tmp;
    }
    return Head;
}
