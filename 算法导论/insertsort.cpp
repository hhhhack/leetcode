#include "Comalg.h"
using namespace std;
void insertSort(vector<int> &input){
    for (int i = 1; i < input.size(); i ++){
        int j = i - 1;
        int key = input[i];
        while(j >= 0 && input[j] > key){
            input[j + 1] = input[j];
            j--;
        }
        input[j + 1] = key;
    }
}